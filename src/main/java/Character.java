public class Character {

	private String name;
	private Alignment alignment;
	
	public void setName(String name){
		this.name = name;
	};
	public String getName(){
		return name;
	};
	
	public void setAlignment(Alignment alignment){
		this.alignment = alignment;
	};
	
	public Alignment getAlignment(){
		return alignment;
	};
		
}